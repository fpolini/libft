#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	char	c;
	int		i;

	i = 0;
	c = s[0];
	while (s[i] != '\0')
	{
		c = s[i];
		write(fd, &c, 1);
		i = i + 1;
	}
}
