#include "libft.h"

void	ft_putstr(char const *s)
{
	char	c;
	int		i;

	i = 0;
	c = s[0];
	while (s[i] != '\0')
	{
		c = s[i];
		write(1, &c, 1);
		i = i + 1;
	}
}
