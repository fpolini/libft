#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	unsigned int	i;
	size_t			n;

	i = 0;
	n = ft_strlen(s);
	while (s[i] != '\0')
	{
		f(s + i);
		i = i + 1;
	}
}
